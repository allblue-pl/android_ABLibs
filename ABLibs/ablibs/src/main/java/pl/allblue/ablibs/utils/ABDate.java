package pl.allblue.ablibs.utils;

public class ABDate
{

    final static public int Span_Second = 1;
    final static public int Span_Minute = 60 * ABDate.Span_Second;
    final static public int Span_Hour   = 60 * ABDate.Span_Minute;
    final static public int Span_Day    = 24 * ABDate.Span_Hour;
    final static public int Span_Week   = 7 * ABDate.Span_Day;
    final static public int Span_Month  = 30 * ABDate.Span_Day;

}
